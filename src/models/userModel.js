'use strict';

var mongoose = require('mongoose');
var Schema = mongoose.Schema;

//Creating the schema for the users
var UserSchema = new Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
});

module.exports = mongoose.model('users', UserSchema);